block_table_chk$values <- block_data$values %>%
  select(Block_Type) %>%
  group_by(Block_Type) %>%
  summarise(`Block Type Count` = n()) %>%
  ungroup() %>%
  mutate(`Percentage(%)` = (`Block Type Count`/nrow(data())) * 100) %>%
  mutate(`Percentage(%)` = round(`Percentage(%)`, digits = 2)) %>%
  arrange(`Block Type Count`) %>%
  adorn_totals("row")

